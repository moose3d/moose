#include "MooseExceptions.h"
/////////////////////////////////////////////////////////////////
Moose::Exceptions::CMooseException::CMooseException( const char *szReason )
{
    if ( szReason ) m_Reason = szReason;
}
/////////////////////////////////////////////////////////////////
const char *
Moose::Exceptions::CMooseException::what() const throw()
{
  return m_Reason.c_str();
}
